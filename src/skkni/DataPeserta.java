    public void tampil_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("UJK");
        model.addColumn("NIK");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Tempat Lahir");
        model.addColumn("Tanggal Lahir");
        model.addColumn("Pendidikan Akhir");
        model.addColumn("Nomor HP");
        try{
            String sql = "select data_ujk.nama_ujk, peserta_skkni.nik, peserta_skkni.nama, peserta_skkni.alamat, peserta_skkni.tempat_lahir, peserta_skkni.tanggal_lahir, peserta_skkni.pendidikan_terakhir, peserta_skkni.hp from peserta_skkni inner join data_ujk on data_ujk.id_ujk = peserta_skkni.id_ujk ";
            java.sql.Statement stm =con.createStatement();
            java.sql.ResultSet res =stat.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8)});
            }
            jTable1.setModel(model);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    
