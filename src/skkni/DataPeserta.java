            String sql = "insert into peserta_skkni (id_ujk, nik, nama, alamat, tempat_lahir, tanggal_lahir, pendidikan_terakhir, hp) values ('"+id+"','"+txt_nik.getText()+"','"+txt_nama.getText()+"','"+txt_alamat.getText()+"','"+txt_tmplahir.getText()+"','"+txt_ttl.getText()+"','"+jComboBox2.getSelectedItem()+"','"+txt_hp.getText()+"')";
