    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String nama_ujk =  jTable1.getValueAt(baris, 0).toString();
        jComboBox1.setSelectedItem(nama_ujk);
        String nik = jTable1.getValueAt(baris, 1).toString();
        txt_nik.setText(nik);
        String nama = jTable1.getValueAt(baris, 2).toString();
        txt_nama.setText(nama);
        String alamat = jTable1.getValueAt(baris, 3).toString();
        txt_alamat.setText(alamat);
        String tmpLahir = jTable1.getValueAt(baris, 4).toString();
        txt_tmplahir.setText(tmpLahir);
        String ttl = jTable1.getValueAt(baris, 5).toString();
        txt_ttl.setText(ttl);
        String pendidikan_akhir = jTable1.getValueAt(baris, 6).toString();
        jComboBox2.setSelectedItem(pendidikan_akhir);
        String hp = jTable1.getValueAt(baris, 7).toString();
        txt_hp.setText(hp);
    }//GEN-LAST:event_jTable1MouseClicked

